package com.basicstrong.business;

public abstract class AbstractDemo {
	
	
	/*
	 * This is abstract class. This must contain at least one abstract method.
	 * Abstract class in not fully implemented and the abstract method in it also not
	 * have full implementation.
	 */
	
	// below is the declaration of abstract method.
	public abstract void myMethod(); // This is how we declare the abstract method closed with semicolon;
	
	// Even if we try to give any implementation to this method the compiler will throw the error.
	

}
